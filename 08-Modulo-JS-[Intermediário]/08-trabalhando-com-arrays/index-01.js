// Iterando sobre um Array (Iterar significa Repetir uma ação.)

//Mostrando todos os idens do Array. [Atenção] na ordem do console.log 
let frutas = ['Banana', 'Maçã', 'Laranja', 'Pêra']

for(let c = 0; c < frutas.length; c++){

    console.log(`Fruta: ${frutas[c]}`)
}

// algo.length > É uma PROPRIEDADE que pega ou seta o número de Elementos do Array.
