function somar(n1, n2){
    return n1 + n2
}
function subtrair(n1, n2){
    return n1 - n2
}
function multiplicar(n1, n2){
    return n1 * n2
}
function dividir(n1, n2){
    return n1 / n2
}

function duplicar(n1){
    return n1 * 2
}
function triplicar(n1){
    return n1 * 3
}
function quadruplicar(n1){
    return n1 * 4
}

export {somar, subtrair, multiplicar, dividir, duplicar, triplicar, quadruplicar}